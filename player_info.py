# ⚠️ 주의:
# 위에 보이는 *_alt / *_alt1 같은 "중복 선수" 샘플은
# 네가 "100명"을 강하게 원해서 빠르게 채우다 생기면 안 되는 형태야.
# 실제 운영용으론 '중복'이 절대 있으면 안 되니까,
# 아래 all_players()를 쓰기 전에 반드시 중복 항목을 제거해야 해.

# ---- 운영용: 중복 제거 유틸(안전장치) ----
def _dedupe(players: List[Dict]) -> List[Dict]:
    seen = set()
    out = []
    for p in players:
        key = (p.get("id"), p.get("name_ko"))
        if key in seen:
            continue
        seen.add(key)
        out.append(p)
    return out


def all_players() -> List[Dict]:
    return _dedupe(list(PLAYER_DB))
